import { mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';

const repoRoot = new URL('../', import.meta.url);
const checkMode = process.argv.includes('--check');

const read = (relativePath) => readFileSync(new URL(relativePath, repoRoot), 'utf8');

const extract = (source, pattern, label) => {
  const match = source.match(pattern);
  if (!match?.[1]) {
    throw new Error(`Unable to find ${label}`);
  }
  return match[1].trim();
};

const packageJson = JSON.parse(read('package.json'));
const moduleBazel = read('MODULE.bazel');
const buildBazel = read('BUILD.bazel');
const bazelVersion = read('.bazelversion').trim();
const ciWorkflow = read('.github/workflows/ci.yml');
const publishWorkflow = read('.github/workflows/publish.yml');

const moduleVersion = extract(moduleBazel, /module\([\s\S]*?version = "([^"]+)"/m, 'MODULE.bazel version');
const buildVersion = extract(buildBazel, /npm_package\([\s\S]*?version = "([^"]+)"/m, 'BUILD.bazel npm_package version');
const buildPackageName = extract(buildBazel, /npm_package\([\s\S]*?package = "([^"]+)"/m, 'BUILD.bazel package name');
const modulePnpmVersion = extract(moduleBazel, /pnpm_version = "([^"]+)"/, 'MODULE.bazel pnpm version');
const ciNodeVersions = extract(ciWorkflow, /node_versions:\s*'([^']+)'/, 'CI node versions');
const ciBazelTargets = extract(ciWorkflow, /bazel_targets:\s*"([^"]+)"/, 'CI bazel targets');
const ciPackageDir = extract(ciWorkflow, /package_dir:\s*([^\n]+)/, 'CI package dir');
const publishNodeVersion = extract(publishWorkflow, /publish_node_version:\s*"([^"]+)"/, 'publish node version');
const githubPackageName = extract(publishWorkflow, /github_package_name:\s*"([^"]+)"/, 'GitHub Packages name');
const npmAccess = extract(publishWorkflow, /npm_access:\s*([^\n]+)/, 'npm access');

const countFiles = (directoryUrl) => {
  let count = 0;
  for (const entry of readdirSync(directoryUrl, { withFileTypes: true })) {
    const entryUrl = new URL(`${entry.name}${entry.isDirectory() ? '/' : ''}`, directoryUrl);
    count += entry.isDirectory() ? countFiles(entryUrl) : 1;
  }
  return count;
};

const sourceAreaNotes = {
  adapters: 'Scheduling provider integrations and availability helpers',
  components: 'Svelte checkout and booking UI',
  core: 'Effect-powered orchestration, error types, and utilities',
  lib: 'Small reusable helpers and Acuity iframe listener',
  onboarding: 'Settings-driven onboarding and credential helpers',
  payments: 'Payment adapters and capability contracts',
  reconciliation: 'Alternative-payment matching and webhook helpers',
  stores: 'Svelte runes-based state',
  testing: 'Cassette recording, replay, and masking utilities',
  tests: 'Vitest helpers, mocks, fixtures, and unit coverage',
};

const sourceAreas = readdirSync(new URL('src/', repoRoot), { withFileTypes: true })
  .filter((entry) => entry.isDirectory())
  .map((entry) => ({
    name: entry.name,
    files: countFiles(new URL(`src/${entry.name}/`, repoRoot)),
    note: sourceAreaNotes[entry.name] ?? 'Repo source area',
  }))
  .sort((left, right) => left.name.localeCompare(right.name));

const scriptRows = Object.entries(packageJson.scripts)
  .filter(([name]) =>
    name === 'build' ||
    name === 'check' ||
    name === 'check:release-metadata' ||
    name === 'lint' ||
    name.startsWith('docs:') ||
    name.startsWith('test:'))
  .sort(([left], [right]) => left.localeCompare(right));

const exportRows = Object.entries(packageJson.exports)
  .map(([subpath, target]) => ({
    subpath,
    types: target.types ?? '',
    runtime: target.default ?? '',
    svelte: target.svelte ?? '',
  }))
  .sort((left, right) => left.subpath.localeCompare(right.subpath));

const markdownTable = (headers, rows) => {
  const formatCell = (value) => String(value ?? '').replace(/\|/g, '\\|').replace(/\n/g, '<br>');
  return [
    `| ${headers.join(' | ')} |`,
    `| ${headers.map(() => '---').join(' | ')} |`,
    ...rows.map((row) => `| ${row.map(formatCell).join(' | ')} |`),
  ].join('\n');
};

const generatedBanner = [
  '<!--',
  'DO NOT EDIT: generated by `node scripts/generate-doc-artifacts.mjs`.',
  '-->',
].join('\n');

const renderPackageSurface = () => `${generatedBanner}

# Package Surface

Generated from \`package.json\` and the current \`src/\` tree.

## Package Identity

${markdownTable(
  ['Field', 'Value'],
  [
    ['Package', `\`${packageJson.name}\``],
    ['Version', `\`${packageJson.version}\``],
    ['Description', packageJson.description],
    ['Node range', `\`${packageJson.engines.node}\``],
    ['Repository', packageJson.repository.url.replace(/^git\+/, '')],
  ],
)}

## Export Map

${markdownTable(
  ['Export', 'Types', 'Runtime', 'Svelte'],
  exportRows.map((row) => [
    `\`${row.subpath}\``,
    row.types ? `\`${row.types}\`` : '',
    row.runtime ? `\`${row.runtime}\`` : '',
    row.svelte ? `\`${row.svelte}\`` : '',
  ]),
)}

## Source Inventory

${markdownTable(
  ['Area', 'Files', 'Notes'],
  sourceAreas.map((row) => [`\`src/${row.name}\``, String(row.files), row.note]),
)}

## Script Entry Points

${markdownTable(
  ['Script', 'Command'],
  scriptRows.map(([name, command]) => [`\`${name}\``, `\`${command}\``]),
)}
`;

const renderReleaseMetadata = () => `${generatedBanner}

# Release Metadata

Generated from \`package.json\`, \`MODULE.bazel\`, \`BUILD.bazel\`,
\`.bazelversion\`, and the repo workflows.

## Version Alignment

${markdownTable(
  ['Surface', 'Value'],
  [
    ['package.json version', `\`${packageJson.version}\``],
    ['MODULE.bazel version', `\`${moduleVersion}\``],
    ['BUILD.bazel npm_package version', `\`${buildVersion}\``],
    ['BUILD.bazel package name', `\`${buildPackageName}\``],
    ['.bazelversion', `\`${bazelVersion}\``],
    ['pnpm packageManager', `\`${packageJson.packageManager}\``],
    ['MODULE.bazel pnpm version', `\`${modulePnpmVersion}\``],
  ],
)}

## Publish And Workflow Inputs

${markdownTable(
  ['Field', 'Value'],
  [
    ['npm package', `\`${packageJson.name}\``],
    ['npm access', `\`${npmAccess}\``],
    ['GitHub Packages name', `\`${githubPackageName}\``],
    ['CI node versions', `\`${ciNodeVersions}\``],
    ['Publish node version', `\`${publishNodeVersion}\``],
    ['Bazel targets', `\`${ciBazelTargets}\``],
    ['Bazel package dir', `\`${ciPackageDir}\``],
  ],
)}

## Validation Commands

${markdownTable(
  ['Check', 'Command'],
  [
    ['Release metadata alignment', '`pnpm check:release-metadata`'],
    ['Typecheck', '`pnpm check`'],
    ['Lint', '`pnpm lint`'],
    ['Unit tests', '`pnpm test:unit`'],
    ['Integration tests', '`pnpm test:integration`'],
    ['Package build', '`pnpm build`'],
    ['Publint', '`pnpm exec publint`'],
    ['Bazel package', '`bazel build //:pkg`'],
    ['MkDocs validation', '`pnpm docs:check`'],
    ['Nix derivation checks', '`nix flake check`'],
  ],
)}
`;

const llmsText = `${generatedBanner}

# scheduling-kit

> Backend-agnostic scheduling library with Effect-powered orchestration, Svelte checkout components, scheduling adapters, and Bazel-backed package artifact checks.

This repository keeps pnpm as the local package-manager and script interface while Bazel defines and builds the publishable package artifact used by CI.

## Docs

- [Docs home](docs/index.md): Repo scope, ownership boundaries, and navigation.
- [Build and release](docs/build-and-release.md): Fresh-machine bootstrap, Bazel/Nix usage, and release hygiene.
- [Testing](docs/testing.md): Actual test layout, commands, and Effect-based helper patterns.
- [Tracing](docs/tracing.md): Cassette recording and replay details.

## Generated Reference

- [Package surface](docs/generated/package-surface.md): Export map, source inventory, and script entry points derived from repo files.
- [Release metadata](docs/generated/release-metadata.md): Version, workflow, and publish metadata derived from repo files.

## Optional

- [README](README.md): Package overview and usage examples.
- [AGENTS](AGENTS.md): Operator runbook and repo boundaries for AI agents and maintainers.
`;

const outputs = new Map([
  ['docs/generated/package-surface.md', renderPackageSurface()],
  ['docs/generated/release-metadata.md', renderReleaseMetadata()],
  ['llms.txt', llmsText],
]);

let hadChanges = false;

for (const [relativePath, nextContent] of outputs) {
  const fileUrl = new URL(relativePath, repoRoot);
  const absolutePath = fileURLToPath(fileUrl);
  mkdirSync(new URL('./', fileUrl), { recursive: true });

  let currentContent = null;
  try {
    currentContent = read(relativePath);
  } catch {
    currentContent = null;
  }

  if (currentContent === nextContent) {
    continue;
  }

  hadChanges = true;
  if (checkMode) {
    console.error(`Generated artifact is out of date: ${absolutePath}`);
    continue;
  }

  writeFileSync(fileUrl, nextContent);
  console.log(`updated ${relativePath}`);
}

if (checkMode && hadChanges) {
  process.exit(1);
}
